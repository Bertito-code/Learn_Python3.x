# En este bucle se crea la variable letra la cual va a contener los valores de la palabra Hola,
# en cada pasada va a imprimir un valor, empezando por la H de Hola. 
# En python el primer valor del indice es 0, con lo cual H vale 0,o vale 1....

for letra in "Hola":  
    print(letra)      
else:
    print("Fin bucle for")  # Imprimimos este mensaje cuando se salga del bucle. 


